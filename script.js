    const cart = [];

    function displayArtworks() {
        const galleryContainer = document.getElementById('galleryContainer');
        galleryContainer.innerHTML = '';
        artworks.forEach(artwork => {
            const artworkElement = document.createElement('div');
            artworkElement.classList.add('artwork');
            artworkElement.innerHTML = `
                <img src="${artwork.image}" alt="${artwork.name}">
                <div class="artwork-details">
                    <h3 class="artwork-name">${artwork.name}</h3>
                    <p class="artwork-price">${artwork.price}</p>
                    <p class="artwork-category">${artwork.category}</p>
                    <button class="add-to-cart-btn" onclick="addToCart('${artwork.name}', '${artwork.price}', '${artwork.image}')">Add to Cart ðŸ›’</button>
                </div>
            `;
            galleryContainer.appendChild(artworkElement);
        });
    }

    function addToCart(name, price, image) {
        cart.push({ name, price, image });
        updateCart();
    }

    function removeFromCart(index) {
        cart.splice(index, 1);
        updateCart();
    }

    function updateCart() {
        const cartItems = document.getElementById('cartItems');
        const cartTotal = document.getElementById('cartTotal');
        cartItems.innerHTML = '';
        let total = 0;
        cart.forEach((item, index) => {
            const cartItem = document.createElement('div');
            cartItem.classList.add('cart-item');
            cartItem.innerHTML = `
                <img src="${item.image}" alt="${item.name}">
                <div>
                    <p class="cart-item-name">${item.name}</p>
                    <p>${item.price}</p>
                </div>
                <button class="cart-item-remove" onclick="removeFromCart(${index})">Remove</button>
            `;
            cartItems.appendChild(cartItem);
            total += parseInt(item.price.substring(1));
        });
        cartTotal.textContent = `Total: $${total}`;
    }


    function searchArtworks() {
        const searchInput = document.getElementById('searchInput').value.toLowerCase();
        const filteredArtworks = artworks.filter((artwork) => artwork.name.toLowerCase().includes(searchInput));
        displayArtworks(filteredArtworks);
    }

